package com.youareright.model.sys;

public class ShelfEntity {
	private int shelfID;
	private int shelfClassID;
	private String shelfPath;
	private String XMLPath;
	
	public int getShelfID() {
		return shelfID;
	}
	public void setShelfID(int shelfID) {
		this.shelfID = shelfID;
	}
	public int getShelfClassID() {
		return shelfClassID;
	}
	public void setShelfClassID(int shelfClassID) {
		this.shelfClassID = shelfClassID;
	}
	public String getShelfPath() {
		return shelfPath;
	}
	public void setShelfPath(String shelfPath) {
		this.shelfPath = shelfPath;
	}
	public String getXMLPath() {
		return XMLPath;
	}
	public void setXMLPath(String xMLPath) {
		XMLPath = xMLPath;
	}
	
	
}
