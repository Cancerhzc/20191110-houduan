package com.youareright.model.sys;

public class ShelfClassEntity {
	private int shelfClassID;
	private String shelfClassName;
	private String shelfClassContent;
	
	public int getShelfClassID() {
		return shelfClassID;
	}
	public void setShelfClassID(int shelfClassID) {
		this.shelfClassID = shelfClassID;
	}
	public String getShelfClassName() {
		return shelfClassName;
	}
	public void setShelfClassName(String shelfClassName) {
		this.shelfClassName = shelfClassName;
	}
	public String getShelfClassContent() {
		return shelfClassContent;
	}
	public void setShelfClassContent(String shelfClassContent) {
		this.shelfClassContent = shelfClassContent;
	}	
}
